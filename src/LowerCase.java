//a Java program to convert a given string into lowercase.
//THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG.

public class LowerCase {
    public static void main(String[] args) {
        String str1= "HE QUICK BROWN FOX JUMPS OVER THE LAZY DOG.";
        // convert all upper case letters in to lower case letters
        System.out.println("Result : " + str1.toLowerCase() );
    }
}
